const { join } = require('path')
const { test } = require('node:test')
const assert = require('node:assert')
const sut = join(process.cwd(), 'src', 'lib', '_sandbox-version')
const { versionGTE } = require(sut)

test('versionGTE', () => {
  assert.strictEqual(versionGTE('1.0.0', '1.0.0'), true, 'equal')
  assert.strictEqual(versionGTE('1.0.0', '1.0.1'), false, 'less than patch')
  assert.strictEqual(versionGTE('1.0.1', '1.0.0'), true, 'greater than patch')
  assert.strictEqual(versionGTE('1.0.0', '1.1.0'), false, 'less than minor')
  assert.strictEqual(versionGTE('1.0.0', '2.0.0'), false, 'less than major')
  assert.strictEqual(versionGTE('1.3.0', '2.2.2'), false, 'less than all')
  assert.strictEqual(versionGTE('2.0.0', '1.0.0'), true, 'greater than')
})
